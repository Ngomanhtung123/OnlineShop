    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int pageNumBer = 1;
        String pageNum = request.getParameter("pageNum");
        if(pageNum != null){
            pageNumBer = Integer.parseInt(pageNum);
        }
        String status = request.getParameter("status");
        String search = request.getParameter("search");
        SliderDao sliderDao = new SliderDao();
        List<Slider> list = sliderDao.getAllSliderWithSearchCondition(status, search);
        List<Slider> listPaging = sliderDao.Paging(list, pageNumBer, 4);
        request.setAttribute("pageNum", pageNumBer);
        request.setAttribute("total", list.size() % 4 == 0 ? list.size() / 4 : (list.size() / 4 + 1));
        request.setAttribute("list", listPaging);
        request.getRequestDispatcher("SlidersList.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

